    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try{
        if (!txtIdProducto.getText().equalsIgnoreCase("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro de que quieres eliminar el producto?", "Confirmar", 2);

            if (confirmacion == 0) {
                FProducto func = new FProducto();
                VProducto dts = new VProducto();

                dts.setIdProducto(Integer.parseInt(txtIdProducto.getText()));
                func.Eliminar(dts);
                Mostrar("");
                Inhabilitar();
            }
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Amigo, tienes un error aquí => " + e);
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

