    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtIdProducto.getText().equals(null)) {
            JOptionPane.showMessageDialog(rootPane, "idProducto vacío");
        }

        if (txtNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un numero de producto");
            txtNombre.requestFocus();
            return;
        }
        if (txtDescripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripción para el producto");
            txtDescripcion.requestFocus();
            return;
        }
        if (txtPrecioVenta.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio diario para el producto");
            txtPrecioVenta.requestFocus();
            return;
        }

        VProducto dts = new VProducto();
        FProducto func = new FProducto();

        dts.setNombre(txtNombre.getText());

        dts.setDescripcion(txtDescripcion.getText());
        dts.setPrecioVenta(Double.parseDouble(txtPrecioVenta.getText()));

         int seleccionado = cboUnidadMedida.getSelectedIndex();
        dts.setUnidadDeMedida((String)cboUnidadMedida.getItemAt(seleccionado));

        try{
            if (accion.equalsIgnoreCase("Guardar")) {
                if (func.Insertar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "El producto fué registrado satisfactoriamente.");
                    Mostrar("");
                    Inhabilitar();
                }
            }else if (accion.equalsIgnoreCase("editar")) {
                dts.setIdProducto(Integer.parseInt(txtIdProducto.getText()));

                if (func.Editar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "El producto fué editado satisfactoriamente.");
                    Mostrar("");
                    Inhabilitar();

                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un error : " + e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

