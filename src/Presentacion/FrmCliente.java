    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtIdPersona.getText().equals(null)) {
            JOptionPane.showMessageDialog(rootPane, "idPersona vacío");
        }

        if (txtNombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre de cliente");
            txtNombre.requestFocus();
            return;
        }
        if (txtAPaterno.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido paterno para el cliente");
            txtAPaterno.requestFocus();
            return;
        }
        if (txtAMaterno.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido materno para el cliente");
            txtAMaterno.requestFocus();
            return;
        }
        if (txtNumeroDoc.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un número de documento para el cliente");
            txtNumeroDoc.requestFocus();
            return;
        }
        if (txtCodCliente.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un código de cliente");
            txtCodCliente.requestFocus();
            return;
        }

        VCliente dts = new VCliente();
        FCliente func = new FCliente();

        dts.setNombre(txtNombre.getText());

        dts.setaPaterno(txtAPaterno.getText());
        dts.setaMaterno(txtAMaterno.getText());

        int seleccionado = cboTipoDoc.getSelectedIndex();
        dts.setTipoDocumento((String)cboTipoDoc.getItemAt(seleccionado));
        dts.setNumDocumento(txtNumeroDoc.getText());
        dts.setDireccion(txtDireccion.getText());
        dts.setTelefono(txtTelefono.getText());
        dts.setEmail(txtEmail.getText());
        dts.setCodigoCliente(txtCodCliente.getText());

        try{
            if (accion.equalsIgnoreCase("Guardar")) {
                if (func.Insertar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "El cliente fué registrado satisfactoriamente.");
                    Mostrar("");
                    Inhabilitar();
                }
            }else if (accion.equalsIgnoreCase("editar")) {
                dts.setIdPersona(Integer.parseInt(txtIdPersona.getText()));

                if (func.Editar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "El cliente fué editado satisfactoriamente.");
                    Mostrar("");
                    Inhabilitar();

                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un error : " + e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

