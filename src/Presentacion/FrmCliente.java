    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try{
            if (!txtIdPersona.getText().equalsIgnoreCase("")) {
                int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro de que quieres eliminar el Cliente?", "Confirmar", 2);

                if (confirmacion == 0) {
                    FCliente func = new FCliente();
                    VCliente dts = new VCliente();

                    dts.setIdPersona(Integer.parseInt(txtIdPersona.getText()));
                    func.Eliminar(dts);
                    Mostrar("");
                    Inhabilitar();
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Amigo, tienes un error aquí => " + e);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

