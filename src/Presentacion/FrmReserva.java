    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        if (txtIdHabitacion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una habitacion");
            txtIdHabitacion.requestFocus();
            return;
        }
        if (txtIdCliente.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un cliente");
            txtIdCliente.requestFocus();
            return;
        }
        if (txtCostoAlojamiento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un costo de alojamiento");
            txtCostoAlojamiento.requestFocus();
            return;
        }

        VReserva dts = new VReserva();
        FReserva func = new FReserva();

        dts.setIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));
        dts.setIdCliente(Integer.parseInt(txtIdCliente.getText()));
        dts.setIdTrabajador(idUsuario);

        int seleccionado = cboTipoReserva.getSelectedIndex();
        dts.setTipoDeReserva((String)cboTipoReserva.getItemAt(seleccionado));
        
        Calendar cal;
        int d,m,a;
        cal = DCFechaReserva.getCalendar();
        d =  cal.get(Calendar.DAY_OF_MONTH);
        m =  cal.get(Calendar.MONTH);
        a =  cal.get(Calendar.YEAR) - 1900;
        dts.setFechaReserva(new Date(a,m,d));
        
        cal = DCFechaIngreso.getCalendar();
        d =  cal.get(Calendar.DAY_OF_MONTH);
        m =  cal.get(Calendar.MONTH);
        a =  cal.get(Calendar.YEAR) - 1900;
        dts.setFechaIngreso(new Date(a,m,d));
        
        cal = DCFechaSalida.getCalendar();
        d =  cal.get(Calendar.DAY_OF_MONTH);
        m =  cal.get(Calendar.MONTH);
        a =  cal.get(Calendar.YEAR) - 1900;
        dts.setFechaEgreso(new Date(a,m,d));
        
        dts.setCostoAlojamiento(Double.parseDouble(txtCostoAlojamiento.getText()));
        seleccionado = cboEstadoDeReserva.getSelectedIndex();
        dts.setEstado((String)cboEstadoDeReserva.getItemAt(seleccionado));

        try{
            if (accion.equalsIgnoreCase("Guardar")) {
                if (func.Insertar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "La reserva fué registrada satisfactoriamente.");
                    Mostrar("");
                    Inhabilitar();
                    
                    //Ocupamos la habitacion alquilada
                    FHabitacion func3 = new FHabitacion();
                    VHabitacion dts3  =  new VHabitacion();
                    
                    dts3.setIdHabitacion(Integer.parseInt(txtIdHabitacion.getText()));
                    func3.Ocupar(dts3);
                }
            }else if (accion.equalsIgnoreCase("editar")) {
                dts.setIdReserva(Integer.parseInt(txtIdReserva.getText()));
                dts.setIdTrabajador(Integer.parseInt(txtIdTrabajador.getText()));

                if (func.Editar(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "La reserva fué editada satisfactoriamente.");
                    Mostrar("");
                    Inhabilitar();

                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Ocurrio un error  en el metodo btnGuardarActionPerformed de la clase FrmReserva: " + e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

