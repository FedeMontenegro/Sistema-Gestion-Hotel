    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try{
            if (!txtIdReserva.getText().equalsIgnoreCase("")) {
                int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro de que quieres eliminar la reserva?", "Confirmar", 2);

                if (confirmacion == 0) {
                    FReserva func = new FReserva();
                    VReserva dts = new VReserva();

                    dts.setIdReserva(Integer.parseInt(txtIdReserva.getText()));
                    func.Eliminar(dts);
                    Mostrar("");
                    Inhabilitar();
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Amigo, tienes un error aquí => " + e);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

